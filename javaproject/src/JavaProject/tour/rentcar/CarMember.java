package JavaProject.tour.rentcar;

public class CarMember {

	   private String pickArea;
	   private String returnArea;
	   private String useTime;
	   private int price;
	   
	   
	public String getPickArea() {
		return pickArea;
	}
	public void setPickArea(String pickArea) {
		this.pickArea = pickArea;
	}
	public String getReturnArea() {
		return returnArea;
	}
	public void setReturnArea(String returnArea) {
		this.returnArea = returnArea;
	}
	public String getUseTime() {
		return useTime;
	}
	public void setUseTime(String useTime) {
		this.useTime = useTime;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	   
	   
}
